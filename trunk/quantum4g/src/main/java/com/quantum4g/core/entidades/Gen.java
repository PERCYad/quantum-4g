public class Gen {

    private Integer numeroGen;

    private Boolean valor;

    private Double gradoBondad;

    private Triada triada;
    
    public Integer getNumeroGen() {
        return numeroGen;
    }

    public void setNumeroGen(Integer numeroGen) {
        this.numeroGen = numeroGen;
    }

    public Boolean getValor() {
        return valor;
    }

    public void setValor(Boolean valor) {
        this.valor = valor;
    }

    public Double getGradoBondad() {
        return gradoBondad;
    }

    public void setGradoBondad(Double gradoBondad) {
        this.gradoBondad = gradoBondad;
    }

    public Triada getTriada() {
        return triada;
    }

    public void setTriada(Triada triada) {
        this.triada = triada;
    }

}
