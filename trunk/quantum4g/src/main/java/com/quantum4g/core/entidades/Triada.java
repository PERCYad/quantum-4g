package com.quantum4g.core.entidades;

/**
 *
 * @author Pepe
 */
public class Triada {

    private Double factorPositivo;

    private Double factorNegativo;

    private Double factorPonderacion;

    public Double getFactorPositivo() {
        return factorPositivo;
    }

    public void setFactorPositivo(Double factorPositivo) {
        this.factorPositivo = factorPositivo;
    }

    public Double getFactorNegativo() {
        return factorNegativo;
    }

    public void setFactorNegativo(Double factorNegativo) {
        this.factorNegativo = factorNegativo;
    }

    public Double getFactorPonderacion() {
        return factorPonderacion;
    }

    public void setFactorPonderacion(Double factorPonderacion) {
        this.factorPonderacion = factorPonderacion;
    }

}
